            // Place the menu just below the header with a small gap
            var top = Math.round(rect.bottom + 8);
            // Apply with !important so it overrides stylesheet !important
            try {
                menu.style.setProperty('position', 'fixed', 'important');
                menu.style.setProperty('top', top + 'px', 'important');
                menu.style.setProperty('right', '0.75rem', 'important');
                menu.style.removeProperty('left');
